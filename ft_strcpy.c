#include "libft.h"

char *ft_strcpy(char *dest, const char *src)
{
    int	i;
    
	i = 0;
	while (*src != '\0')
	{
		*(dest + i) = *src;
		src++;
		i++;
	}
	*(dest + i) = *src;
	return (dest);
}

